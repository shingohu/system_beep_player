import 'beep_sound_platform_interface.dart';

class SystemBeepPlayer {
  SystemBeepPlayer._();

  ///[soundId] from AndroidSoundIDs or IOSSoundID
  ///[volume] volume 0-100
  ///[durationMs] play duration in milliseconds
  static Future<void> play(
    int soundId, {
    int volume = 100,
    int durationMs = 200,
    AndroidStreamType streamType = AndroidStreamType.music,
  }) {
    return BeepSoundPlatform.instance.playSysSound(
      soundId,
      volume: volume,
      durationMs: durationMs,
      streamType: streamType.value,
    );
  }
}

